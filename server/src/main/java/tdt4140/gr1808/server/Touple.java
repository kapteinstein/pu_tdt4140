package tdt4140.gr1808.server;

public class Touple {
	
	private String puls;
	private String dateTime;
	
	public Touple(String puls, String dateTime) {
		this.puls = puls;
		this.dateTime = dateTime;
	}
	
	public String getPuls() {
		return puls;
	}
	
	public String getDateTime() {
		return dateTime;
	}

}
